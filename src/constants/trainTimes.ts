export interface HoursAndMinutes {
    hour: number;
    minute: number;
    str: string;
}

export interface TrainRoute {
    startTime: HoursAndMinutes;
    endTime: HoursAndMinutes;
}

function convertToHoursAndMinutes(str: string): HoursAndMinutes {
    const split = str.split(":");
    const hoursStr = split[0];
    const minStr = split[1];
    return {
        hour: parseInt(hoursStr),
        minute: parseInt(minStr),
        str: str
    };
}

function convertToTrainRoute(list: string[]): TrainRoute {
    return {
        startTime: convertToHoursAndMinutes(list[0]),
        endTime: convertToHoursAndMinutes(list[1])
    };
}

